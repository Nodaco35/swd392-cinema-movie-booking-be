import { DataTypes } from "sequelize";
import { sequelize } from "../config/sequelize.js";

const Cinema = sequelize.define(
  "Cinema",
  {
    id: {
      type: DataTypes.INTEGER,
      primaryKey: true,
      autoIncrement: true,
    },
    name: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    city: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    address: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    phone: {
      type: DataTypes.STRING,
      allowNull: false,
    },
  },
  {
    tableName: "cinemas",
    timestamps: false,
  },
);

export default Cinema;
